
weekdays = ['mon','tues', 'wed', 'thurs'] 

console.log(weekdays);  

console.log('-----------------------');

weekdays.push('fri')
console.log(weekdays); // add new item to the end of the array

console.log('-----------------------');

weekdays.pop()
console.log(weekdays); // removes the last item from the array

console.log('-----------------------');

weekdays.unshift('sun')
console.log(weekdays);// add new item to the start of the array
 
console.log('-----------------------');

weekdays.shift('sun')
console.log(weekdays); // removes first item from the array

console.log('------------------------'); 

weekdays.push(3)
console.log(weekdays);



